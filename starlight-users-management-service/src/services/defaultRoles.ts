/**
 * It creates a list of roles with the default permissions for each role
 * @param {string} tenantId - The tenant ID of the tenant to create the default roles for.
 * @returns an object with two properties:
 * - `roles`: an array of roles
 * - `policyTemplates`: an array of policy templates
 */
export const getDefaultRolesForTenant = (
  tenantId: string,
): {
  roles: Role[];
  policyTemplates: RolePolicyTemplate[];
} => {
  const roles: Role[] = [];

  rolesConfig.forEach((config) => {
    const role = Role.merge(new Role(), { tenantId, description: config.name });

    role.policyTemplates = Object.entries(config.permissions).map(([type, actions]) => {
      const template = RolePolicyTemplate.merge(new RolePolicyTemplate(), {
        access: adaptActionsList(actions),
        resourceType: type as ResourceType,
      });

      template.role = role;

      return template;
    });

    roles.push(role);
  });

  const policyTemplates = roles.flatMap((role) => role.policyTemplates);

  return { roles, policyTemplates };
};
